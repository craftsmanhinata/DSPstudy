%正交移相器的延时
%(一)程序说明 本程序主要用于观察正交移相器的延时

clc;
clear;
close all;

N1=200;   % 信号长度
N=31;      %  正交移相器阶数
M=(N-1)/2;    %  延迟单元长度
h(1:N)=0; 
h(1:2:31)=2./(((0:2:30)-15)*pi); 
x=sin(0.0625*pi*(0:N1-1));    %  输入信号
y=conv(h,x);        %  求正交移相器输出
x1(1:N1+M)=0;
x1(M+1:N1+M)=x;        %  将输入信号延迟
a=0:N1-101; 
figure;
plot(a,x1(1:N1-100),'k',a,y(1:N1-100),'r--','LineWidth',2);
grid on; 
xlabel('n');
ylabel('时域波形'); 
legend('x(n)','y(n)');
%本程序用于由IIR数字滤波器的系统的零、极点求系统级联结构，并求系统输出。

clc;
clear;
close all;

x=sin(2*pi*(0:199)*1/20)+cos(2*pi*(0:199)*5/20);%系统输入
z=[  1.0002  1.0000  +  0.0002i  1.0000  -  0.0002i  0.9998  ];%系统零点
p=[ 0.2266 + 0.6442i 0.2266 - 0.6442i 0.1645 + 0.1937i 0.1645 - 0.1937i]; %系统极点
k=0.1672;%系统增益

sos=zp2sos(z,p,k);%求系统级联结构系数矩阵
y=sosfilt(sos,x);%求系统输出

figure;plot(x);
figure;plot(y);
